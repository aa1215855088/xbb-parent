public class StringUtil {

    public static String productAttribute(Object[] id) {
        StringBuilder attribute = new StringBuilder();
        for (Object obj: id) {
            attribute.append(obj);
            attribute.append(",");
        }
        return attribute.substring(0, attribute.length() - 1);
    }
}
